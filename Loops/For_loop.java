package Loops;

public class For_loop {
    public static void main(String[] args) {
        // increment
        System.out.println("Increment");
        for (int i = 1; i<=10; i++){
            System.out.println(i);
        }

        // decrement
        System.out.println("Decrement");
        for (int i = 10; i>0; i--){
            System.out.println(i);
    }
}
}
